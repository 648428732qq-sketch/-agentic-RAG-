SYMPTOM_TERMS = sorted(SYMPTOM_ALIASES, key=len, reverse=True)
SYMPTOM_STOPWORDS = {"量多", "微有"}


def clean_text(text: str) -> str:
    text = text.replace("\u3000", " ")
    text = re.sub(r"[ \t]+", " ", text)
    text = re.sub(r"\n{3,}", "\n\n", text)
    return text.strip()


def unique(items: list[str]) -> list[str]:
    seen: set[str] = set()
    out: list[str] = []
    for item in items:
        value = clean_text(item)
        if not value or value in seen:
            continue
        seen.add(value)
        out.append(value)
    return out


def find_non_overlapping_aliases(text: str) -> list[tuple[str, str]]:
    matches: list[tuple[int, int, str, str]] = []
    for canonical, aliases in SYMPTOM_ALIASES.items():
        for phrase in [canonical, *aliases]:
            start = text.find(phrase)
            while start != -1:
                end = start + len(phrase)
                matches.append((start, end, canonical, phrase))
                start = text.find(phrase, start + 1)

    selected: list[tuple[int, int, str, str]] = []
    for match in sorted(matches, key=lambda item: (item[1] - item[0], item[0]), reverse=True):
        start, end, _, _ = match
        if any(start < selected_end and end > selected_start for selected_start, selected_end, _, _ in selected):
            continue
        selected.append(match)

    selected.sort(key=lambda item: item[0])
    return [(canonical, phrase) for _, _, canonical, phrase in selected]


def expand_symptom_query(query: str) -> dict[str, list[str] | str]:
    normalized = clean_text(query)
    canonical_terms: list[str] = []
    colloquial_terms: list[str] = []

    for canonical, phrase in find_non_overlapping_aliases(normalized):
        canonical_terms.append(canonical)
        if phrase != canonical:
            colloquial_terms.append(phrase)

    canonical_terms = unique(canonical_terms)
    colloquial_terms = unique(colloquial_terms)
    expanded = clean_text(
        "\n".join(
            [
                f"用户口语症状 {normalized}",
                "标准中医表征 " + " ".join(canonical_terms),
                "口语近义表达 " + " ".join(colloquial_terms),
            ]
        )
    )
    return {
        "original_query": normalized,
        "canonical_terms": canonical_terms,
        "colloquial_terms": colloquial_terms,
        "expanded_query": expanded,
    }


def symptom_overlap_score(payload: dict[str, Any], canonical_terms: list[str], colloquial_terms: list[str]) -> int:
    fields: list[str] = []
    for key in (
        "ancient_symptoms",
        "modern_symptoms",
        "symptom_aliases",
        "diagnostic_keys",
        "pathogenesis",
        "treatment_method",
        "acupoints_or_channels",
        "intervention_name",
        "herb_name",
        "herb_aliases",
        "nature_flavor",
        "property_text",
        "theory_topic",
        "theory_question",
        "theory_answer",
        "theory_terms",
        "diagnostic_method",
        "acupuncture_principle",
        "acupuncture_terms",
        "indications",
    ):
        value = payload.get(key, [])
        if isinstance(value, list):
            fields.extend(str(item) for item in value)
        elif value:
            fields.append(str(value))
    haystack = " ".join(fields)
    return sum(1 for term in canonical_terms + colloquial_terms if term and term in haystack)
